package com.example.zhangnan.myfarm.activity_information;

public class salt {
	private int id;
	private float us;
	private float mg;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public float getUs() {
		return us;
	}
	public void setUs(float us) {
		this.us = us;
	}
	public float getMg() {
		return mg;
	}
	public void setMg(float mg) {
		this.mg = mg;
	}
	
}
